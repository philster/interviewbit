/**
 * Max Product Subarray
 * 
 * Find the contiguous subarray within an array (containing at least one number) which has the largest product.
 * Return an integer corresponding to the maximum product possible.
 * 
 * Example :
 * 
 *   Input : [2, 3, -2, 4]
 *   Return : 6 
 * 
 *   Possible with [2, 3]
 */

module.exports = { 
    //param A : array of integers
    //return an integer
    maxProduct : function(A){
        if (A.length === 1) {
            return A[0];
        }
        // max positive product ending at the current position
        var maxend = A[0];
        // min negative product ending at the current position
        var minend = A[0];
        // max so far
        var maxproduct = A[0];
        for (var i = 1; i < A.length; i++) {
            if (A[i] > 0) {
                maxend = Math.max(A[i], A[i] * maxend);
                minend = Math.min(A[i], A[i] * minend);
            } else {
                var temp = maxend;
                maxend = Math.max(A[i], A[i] * minend);
                minend = Math.min(A[i], A[i] * temp);
            }
            maxproduct = Math.max(maxproduct, maxend);
            // console.log('>> ' + A[i] + ', temp='+temp + ', maxproduct='+maxproduct + ', maxend='+maxend);
        }
        return maxproduct;
    }
};

